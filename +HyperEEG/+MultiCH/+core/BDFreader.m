function EEG = BDFreader(inputDir)
% BDFREADER 读取BDF/EDF格式EEG数据（基于EEGLAB + BIOSIG）
%
% 输入：
%   inputDir - 文件路径（char 或 string）
%
% 输出：
%   EEG - EEGLAB标准EEG结构体
%
% 功能说明：
%   1. 自动兼容 string / char 路径格式
%   2. 检查文件是否存在
%   3. 强制转换为 char 以兼容旧版 BIOSIG
%   4. 调用 pop_biosig 读取EEG数据
%   5. 提供基础错误捕获与调试信息

    % -------------------------------------------------------
    % 1. 输入检查
    % -------------------------------------------------------
    if nargin < 1
        error('必须提供输入文件路径 inputDir');
    end

    % -------------------------------------------------------
    % 2. 路径格式统一（string -> char）
    % -------------------------------------------------------
    if isstring(inputDir)
        filename = char(inputDir);
    elseif ischar(inputDir)
        filename = inputDir;
    else
        error('输入参数必须为 char 或 string 类型');
    end

    % -------------------------------------------------------
    % 3. 文件存在性检查
    % -------------------------------------------------------
    if ~exist(filename, 'file')
        error('未找到文件：%s', filename);
    end

    % -------------------------------------------------------
    % 4. BIOSIG兼容性处理（关键步骤）
    %    某些版本仅支持 char，不支持 string
    % -------------------------------------------------------
    filename = char(filename);

    % -------------------------------------------------------
    % 5. 读取EEG数据（EEGLAB + BIOSIG）
    % -------------------------------------------------------
    try
        EEG = pop_biosig(filename);

    catch ME
        % ---------------------------------------------------
        % 6. 错误捕获与调试信息输出
        % ---------------------------------------------------
        fprintf('\n================ BIOSIG读取错误 ================\n');
        fprintf('文件路径：%s\n', filename);
        fprintf('错误信息：%s\n', ME.message);
        fprintf('=================================================\n\n');

        % 重新抛出错误（便于定位问题）
        rethrow(ME);
    end
end